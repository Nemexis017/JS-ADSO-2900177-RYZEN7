 //con Paranetros
 const sumaExp = function(pnumUno, pnumDos){
    let sumar;
 
    let num1 = pnumUno;
    let numDos = pnumDos;
    sumar =  num1 + numDos;
    return sumar;
 
 }
 
 const restaExp = function(pnumUno, pnumDos){
     let resta;
 
     let num1 = pnumUno;
     let numDos = pnumDos;
     resta =  num1 - numDos;
     return resta;
 
 }
 
 const multiplicacionExp = function(pnumUno, pnumDos){
     let multiplicar;
 
     let num1 = pnumUno;
     let numDos = pnumDos;
     multiplicar =  num1 * numDos;
     return multiplicar;
 
 }
 
 const divisionExp = function(pnumUno, pnumDos){
     let dividir;
 
    let num1 = pnumUno;
    let numDos = pnumDos;
    dividir =  num1 / numDos;
    return dividir;
 
 }
 